# Função geradora para o tipo 'int'
num = int(10)
print('Valor:'+ str(num) +' - Tipo:'+ str(type(num)))

#Função geradora para o tipo 'float'
num = float('9.65')
print('Valor:'+ str(num) +' - Tipo:'+ str(type(num)))

#Função geradora para o tipo 'float' com valor infinito positivo
num = float('+inf')
print('Valor:'+ str(num) +' - Tipo:'+ str(type(num)))

#Função geradora para o tipo 'complex'
num = complex(1, 2)
print('Valor:'+ str(num) +' - Tipo:'+ str(type(num)))